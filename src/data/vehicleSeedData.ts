export const getVehicleById = (id: number): Vehicle | undefined => {
  return vehicleSeedData.find(vehicle => vehicle.id === id);
};

export const getVehicleDetailById = (id: number): VehicleDetail | undefined => {
  return vehicleDetailSeedData.find(vehicle => vehicle.id === id);
};

export const getRelatedVehicles = (currentId: number, limit: number = 6): VehicleDetail[] => {
  return vehicleDetailSeedData
    .filter(vehicle => vehicle.id !== currentId)
    .slice(0, limit);
};

export const filterVehiclesByType = (vehicles: Vehicle[], filterType: string): Vehicle[] => {
  if (filterType === 'all') {
    return vehicles;
  }
  
  return vehicles.filter(vehicle =>
    vehicle.type.toLowerCase() === filterType.toLowerCase() ||
    (vehicle.category && vehicle.category.toLowerCase() === filterType.toLowerCase())
  );
};

export const getVehiclesByCategory = (category: string): Vehicle[] => {
  return vehicleSeedData.filter(vehicle =>
    vehicle.category && vehicle.category.toLowerCase() === category.toLowerCase()
  );
};

export const getFeaturedVehicles = (limit: number = 6): Vehicle[] => {
  return vehicleSeedData
    .filter(vehicle => vehicle.rating !== undefined && vehicle.rating >= 4.7)
    .slice(0, limit);
};